    <ul type="1">
    	<li class="preguntas"> ¿Qué son las asignaturas empresariales y organizacionales?</li><br />
			Son <strong>asignaturas optativas profesionalizantes y complementarias</strong> que ofrecen a los estudiantes la oportunidad de vincularse e interactuar directamente con los profesionales, ejecutivos, directivos funcionarios, socios y empresarios tanto de empresas privadas como de organismos públicos.<br /><br />

		<li class="preguntas">¿Qué beneficios tiene cursar estas asignaturas?</li><br />
        	El cursar estas asignaturas tiene diversos beneficios como:<br />
            <ul type="disc">
            	<li>Interacción con ejecutivos y directivos</li>
				<li>Conocimientos prácticos</li>
				<li>Participación en el desarrollo de proyectos</li>
				<li>Conferencias y talleres</li>
				<li>Visitas a empresas y organismos</li>
				<li>Constancia con valor curricular</li>
	      </ul><br />
          <li class="preguntas">¿Quiénes imparten las asignaturas empresariales y organizacionales? </li><br />
          		Empresarios, ejecutivos, directores, funcionarios y socios de empresas y organismos del sector público, privado y social que participan en el programa.<br /><br />
           <li class="preguntas">¿Dónde se imparten las asignaturas empresariales y organizacionales?</li><br />
           		Se imparten tanto en instalaciones de la facultad, como en instalaciones de empresas y organismos.<br /><br />
           <li class="preguntas">¿Qué requisitos necesito para inscribir estas asignaturas? </li><br />
           		Ser de sistema escolarizado, tener un promedio mínimo de 8 y contar con disponibilidad de horario al inscribir tus demás asignaturas. También hay que considerar requisitos definidos por cada empresa u organismo.<br /><br />
           <li class="preguntas">¿Cómo me inscribo?</li><br />
           		La inscripción a las asignaturas empresariales y organizacionales es a través de la página de FCA-Administración Escolar <a href="https://meteora.contad.unam.mx/escolar/licenciatura/inscripcion_nuevo/" target="_blank">(clic aqu&iacute;)</a>, a la hora de tu inscripción verás un apartado especial para las asignaturas empresariales y organizacionales, deberás de considerar que los horarios de tus demás asignaturas no se empalmen con el de la(s)  asignatura(s)  empresariales que quieras inscribir. Revisa la convocatoria para conocer las fechas de inscripción.<br /><br />
           <li class="preguntas">¿Cuántas asignaturas empresariales y organizacionales puedo inscribir?</li><br />
           		Todas las optativas que puedas inscribir de acuerdo a tu plan de estudios, la carrera y semestre a cursar.<br /><br />
           <li class="preguntas">¿Las asignaturas empresariales y organizacionales tienen actividades complementarias?</li><br />
           		A lo largo del semestre se realizan visitas a corporativos y plantas productivas tanto en la Ciudad de México como en el interior de la República, así como también platicas, talleres y conferencias de acuerdo a la programación de cada empresa u organismo.<br /><br />
           <li class="preguntas">¿Estas asignaturas son optativas de valor curricular del plan de estudios?</li><br />
           		Sí, las asignaturas empresariales y organizacionales cubren los mismos créditos que cualquier otra asignatura optativa (8 créditos).<br /><br />
           <li class="preguntas">¿Puedo inscribir una asignatura empresarial y organizacional de otra carrera?</li><br />
           		Únicamente podrás inscribir asignaturas que estén ofertadas de acuerdo a tu carrera y semestre correspondientes (considera que hay asignaturas que son ofertadas para dos o tres carreras).<br /><br />
           <li class="preguntas">¿Cómo puedo darme de baja de una asignatura empresarial y organizacional?</li><br />
            	Estas materias se pueden dar de baja durante el periodo de altas y bajas del calendario oficial de administración escolar. Este proceso se llevará a cabo a través de la página de la FCA-Administración escolar. <a href="https://meteora.contad.unam.mx/escolar/licenciatura/inscripcion_nuevo/" target="_blank">(clic aqu&iacute;)</a>	<br /><br />
      	   <li class="preguntas">¿Qué pasa si repruebo una asignatura empresarial y organizacional?</li><br />
           		Como cualquier optativa del plan de estudios podrás recursar la misma asignatura u otra optativa de dicho plan para cubrir los créditos correspondientes.<br />
